number = 9.0 
# บวก
result1 = number + 2
print(result1)
# ลบ
result2 = number - 2
print(result2)
# คูณ
result3 = number * 2
print(result3)
# หาร
result4 = number / 2
print(result4)
# MOD หารเอาเศษ
result5 = number % 2
print(result5)
# ยกกำลัง
result6 = number ** 2
print(result6)
# division หารเอาจำนวนเต็ม
result7 = number // 2
print(result7)